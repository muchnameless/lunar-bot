import { EventEmitter } from 'node:events';
import { setTimeout, clearTimeout } from 'node:timers';
import type { Awaitable } from 'discord.js';
import { ChatBridgeEvent, type ChatBridge } from './ChatBridge.js';
import type { HypixelMessage } from './HypixelMessage.js';

/**
 * Filter to be applied to the collector.
 */
type CollectorFilter = (message: HypixelMessage, collected: HypixelMessage[]) => Awaitable<boolean>;

/**
 * Options to be applied to the collector.
 */
export interface HypixelMessageCollectorOptions {
	filter?: CollectorFilter;
	/**
	 * How long to stop the collector after inactivity in milliseconds
	 */
	idle?: number;
	/**
	 * maximum amount of messages that pass the filter
	 */
	max?: number;
	/**
	 * maximum amount of messages to filter
	 */
	maxProcessed?: number;
	/**
	 * How long to run the collector for in milliseconds
	 */
	time?: number;
}

export const enum HypixelMessageCollectorEvent {
	Collect = 'collect',
	End = 'end',
	Ignore = 'ignore',
}

export interface HypixelMessageCollector {
	on(
		event: HypixelMessageCollectorEvent.Collect | HypixelMessageCollectorEvent.Ignore,
		listener: (item: HypixelMessage) => Awaitable<void>,
	): this;
	on(
		event: HypixelMessageCollectorEvent.End,
		listener: (collected: HypixelMessageCollector['collected'], reason: string) => Awaitable<void>,
	): this;
}

/**
 * MessageCollector
 */
export class HypixelMessageCollector extends EventEmitter {
	/**
	 * The chatBridge that instantiated this Collector
	 */
	public readonly chatBridge: ChatBridge;

	/**
	 * The options of this collector
	 */
	public options: HypixelMessageCollectorOptions;

	/**
	 * The filter applied to this collector
	 */
	public filter: CollectorFilter;

	/**
	 * The items collected by this collector
	 */
	public collected: HypixelMessage[] = [];

	/**
	 * Whether this collector has finished collecting
	 */
	public ended = false;

	/**
	 * Total number of messages that were received from the bot during message collection
	 */
	public received = 0;

	/**
	 * Timeout for cleanup
	 */
	private _timeout: NodeJS.Timeout | null = null;

	/**
	 * Timeout for cleanup due to inactivity
	 */
	private _idletimeout: NodeJS.Timeout | null = null;

	private _endReason: string | null = null;

	public constructor(chatBridge: ChatBridge, options: HypixelMessageCollectorOptions = {}) {
		super();

		this.chatBridge = chatBridge;
		this.options = options;

		this.filter = options.filter ?? (() => true);
		if (typeof this.filter !== 'function') {
			throw new TypeError('INVALID_TYPE: options.filter is not a function');
		}

		this.chatBridge.incrementMaxListeners();
		this.chatBridge.on(ChatBridgeEvent.Message, this._handleCollect);
		this.chatBridge.once(ChatBridgeEvent.Disconnect, this._handleBotDisconnection);

		this.once(HypixelMessageCollectorEvent.End, () => {
			this.chatBridge.off(ChatBridgeEvent.Message, this._handleCollect);
			this.chatBridge.off(ChatBridgeEvent.Disconnect, this._handleBotDisconnection);
			this.chatBridge.decrementMaxListeners();
		});

		if (options.time) this._timeout = setTimeout(() => this.stop('time'), options.time);
		if (options.idle) this._idletimeout = setTimeout(() => this.stop('idle'), options.idle);
	}

	/**
	 * Checks after un/collection to see if the collector is done.
	 */
	public get endReason() {
		if (this.options.max && this.collected.length >= this.options.max) return 'limit';
		if (this.options.maxProcessed && this.received === this.options.maxProcessed) return 'processedLimit';
		return this._endReason;
	}

	/**
	 * Returns a promise that resolves with the next collected element;
	 * rejects with collected elements if the collector finishes without receiving a next element
	 */
	public get next(): Promise<HypixelMessage> {
		return new Promise((resolve, reject) => {
			if (this.ended) {
				reject(this.collected);
				return;
			}

			const cleanup = () => {
				// eslint-disable-next-line @typescript-eslint/no-use-before-define
				this.off(HypixelMessageCollectorEvent.Collect, onCollect);
				// eslint-disable-next-line @typescript-eslint/no-use-before-define
				this.off(HypixelMessageCollectorEvent.End, onEnd);
			};

			const onCollect = (item: HypixelMessage) => {
				cleanup();
				resolve(item);
			};

			const onEnd = () => {
				cleanup();
				reject(this.collected);
			};

			this.on(HypixelMessageCollectorEvent.Collect, onCollect);
			this.on(HypixelMessageCollectorEvent.End, onEnd);
		});
	}

	/**
	 * handles stopping the collector when the bot got disconnected
	 */
	private readonly _handleBotDisconnection = () => this.stop('disconnect');

	/**
	 * Call this to handle an event as a collectable element
	 *
	 * @param hypixelMessage
	 */
	private readonly _handleCollect = async (hypixelMessage: HypixelMessage) => {
		++this.received;

		// eslint-disable-next-line unicorn/no-array-method-this-argument
		if (await this.filter(hypixelMessage, this.collected)) {
			this.collected.push(hypixelMessage);

			/**
			 * Emitted whenever an element is collected.
			 */
			this.emit(HypixelMessageCollectorEvent.Collect, hypixelMessage);

			if (this._idletimeout) {
				clearTimeout(this._idletimeout);
				this._idletimeout = setTimeout(() => this.stop('idle'), this.options.idle);
			}
		} else {
			/**
			 * Emitted whenever an element is not collected by the collector.
			 */
			this.emit(HypixelMessageCollectorEvent.Ignore, hypixelMessage);
		}

		this.checkEnd();
	};

	/**
	 * Stops this collector and emits the `end` event.
	 *
	 * @param reason - The reason this collector is ending
	 */
	public stop(reason = 'user') {
		if (this.ended) return;

		if (this._timeout) {
			clearTimeout(this._timeout);
			this._timeout = null;
		}

		if (this._idletimeout) {
			clearTimeout(this._idletimeout);
			this._idletimeout = null;
		}

		this._endReason = reason;
		this.ended = true;

		/**
		 * Emitted when the collector is finished collecting.
		 *
		 * @param collected - The elements collected by the collector
		 * @param reason - The reason the collector ended
		 */
		this.emit(HypixelMessageCollectorEvent.End, this.collected, reason);
	}

	/**
	 * Resets the collectors timeout and idle timer.
	 *
	 * @param options - Options
	 * @param options.time - How long to run the collector for in milliseconds
	 * @param options.idle - How long to stop the collector after inactivity in milliseconds
	 */
	public resetTimer({ time, idle }: { idle?: number; time?: number } = {}) {
		if (this._timeout) {
			clearTimeout(this._timeout);
			this._timeout = setTimeout(() => this.stop('time'), time ?? this.options.time);
		}

		if (this._idletimeout) {
			clearTimeout(this._idletimeout);
			this._idletimeout = setTimeout(() => this.stop('idle'), idle ?? this.options.idle);
		}
	}

	/**
	 * Checks whether the collector should end, and if so, ends it.
	 */
	public checkEnd() {
		const reason = this.endReason;
		if (reason) this.stop(reason);
		return Boolean(reason);
	}

	/**
	 * Allows collectors to be consumed with for-await-of loops
	 * https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/for-await...of
	 */
	public async *[Symbol.asyncIterator]() {
		const queue: HypixelMessage[] = [];
		const onCollect = (item: HypixelMessage) => {
			queue.push(item);
		};

		this.on(HypixelMessageCollectorEvent.Collect, onCollect);

		try {
			while (queue.length || !this.ended) {
				if (queue.length) {
					yield queue.shift();
				} else {
					await new Promise((resolve) => {
						const tick = () => {
							this.off(HypixelMessageCollectorEvent.Collect, tick);
							this.off(HypixelMessageCollectorEvent.End, tick);
							resolve(null);
						};

						this.on(HypixelMessageCollectorEvent.Collect, tick);
						this.on(HypixelMessageCollectorEvent.End, tick);
					});
				}
			}
		} finally {
			this.off(HypixelMessageCollectorEvent.Collect, onCollect);
		}
	}
}
